
package ejerciciolistasenlazadas2021;

public class Automovil {
    String codAuto;
    int tam;

    public Automovil(String codAuto, int tam) {
        this.codAuto = codAuto;
        this.tam = tam;
    }

    public String getCodAuto() {
        return codAuto;
    }

    public void setCodAuto(String codAuto) {
        this.codAuto = codAuto;
    }

    public int getTam() {
        return tam;
    }

    public void setTam(int tam) {
        this.tam = tam;
    }

    
}
